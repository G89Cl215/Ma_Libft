int		ft_match(char *s1, char *s2, int is_esc)
{
	if (!(*s1) && !(*s2))
		return (1);
	if (!(is_esc) && *s2 == '\\')
		return (ft_match(s1, s2 + 1, 1));
	if ((*s1) && *s2 == '*' && !(is_esc))
		return (ft_match(s1 + 1, s2, 0) || ft_match(s1, s2 + 1, 0));
	if (!(*s1) && *s2 == '*' && !(is_esc))
		return (ft_match(s1, s2 + 1, 0));
	if ((*s1 == *s2) || (*s2 == '\?' && (*s1) && !(is_esc)))
		return (ft_match(s1 + 1, s2 + 1, 0));
	return (0);
}
